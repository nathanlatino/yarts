package com.framework.enums.states;

public enum MotionState {

    IDLE(0),
    MOVING(1);

    private final int id;

    /*------------------------------------------------------------------*\
    |*							Constructors
    \*------------------------------------------------------------------*/

    MotionState(int id) {
        this.id = id;
    }

    /*------------------------------------------------------------------*\
    |*							Public Methods
    \*------------------------------------------------------------------*/

    /*------------------------------*\
    |*			  Getters
    \*------------------------------*/

    public int getId() {
        return id;
    }
}
